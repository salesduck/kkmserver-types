export enum CommandStatus {
    /**
     * Успешно выполнена
     */
    SUCCESS = 0,

    /**
     * Запущена на выполнение
     */
    RUNNING = 1,

    /**
     * Выполнена с ошибкой
     */
    ERROR = 2,

    /**
     * Команда (или устройство) не найдена
     */
    NOT_FOUND = 3,

    /**
     * Еще не запущена (ожидает принятие устройством)
     */
    NOT_RUNNING = 4
}
